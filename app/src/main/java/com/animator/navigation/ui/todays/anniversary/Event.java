package com.animator.navigation.ui.todays.anniversary;

public class Event {
    String id, name;
    String date;
    String image;
    String imageH;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getImageH() {
        return imageH;
    }

    public void setImageH(String imageH) {
        this.imageH = imageH;
    }
}

